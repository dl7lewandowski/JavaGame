package agh.cs.lab8;

public class AnimalInfo {
    public int identity;
    public int numberChildren;
    public int numberDescendant;
    public int age;
    public int era;
    public int numberEra;

    public AnimalInfo(int identity, int numberChildren, int numberDescendant, int age, int era, int numberEra) {
        this.identity = identity;
        this.numberChildren = numberChildren;
        this.numberDescendant = numberDescendant;
        this.age = age;
        this.era = era;
        this.numberEra = numberEra;
    }
}
